
public class TPC02 {
	public static void main(String[] args) {
		// 프로그래밍의 3대 요소: 변수, 자료형(DataType), 할당(=)
		// 1 + 1 = 2
		int a, b, c;
		a = 1;
		b = 1;
		c = a + b;
		System.out.println(c);
		
		float f; // float, double(기본)
		f = 34.5f;
		System.out.println(f);
		
		char d;
		d = 'A';//홑따옴표
		System.out.println(d);
		
		boolean g;
		g = true;
		System.out.println(g);
		
		//Book bk; 새로운 자료형 클래스로 설계
	}

}
